    private void mostrar(int[][] data) {
        String mat = "";
        int tam = data.length;
        for (int i = 0; i < tam; i++) {
            for (int j = 0; j < tam; j++) {
                mat += j == tam - 1 ? data[i][j] + "\n" : data[i][j] + ", ";
            }
        }

        txtArea.setText(mat);
    }

